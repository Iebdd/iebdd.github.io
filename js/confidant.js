/* Set the width of the sidebar to 167px and the left margin of the page content to 167px */
function openNav() {
  document.getElementById("mySidebar").style.width = "167px";
  document.getElementById("main").style.marginLeft = "167px";
}

/* Set the width of the sidebar to 0 and the left margin of the page content to 0 */
function closeNav() {
  document.getElementById("mySidebar").style.width = "0";
  document.getElementById("main").style.marginLeft = "0";
} 